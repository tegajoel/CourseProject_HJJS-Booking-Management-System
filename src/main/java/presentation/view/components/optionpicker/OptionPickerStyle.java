package presentation.view.components.optionpicker;

/**
 * Style of the options to be displayed
 */
public enum OptionPickerStyle {
    /**
     * Displayed as a vertical list
     */
    VERTICAL,
    /**
     * Displayed as a vertical list with an option to exit the app
     */
    VERTICAL_WITH_EXIT_APP_OPTION,
    /**
     * Displayed as a horizontal list
     */
    HORIZONTAL
}
